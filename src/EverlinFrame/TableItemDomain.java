package EverlinFrame;

public class TableItemDomain {
	protected Integer row = -1;
	protected String polyString = "";
	
	public TableItemDomain() {}
	
	public Integer getRow() {
		return row;
	}

	public void setRow(Integer row) {
		this.row = row;
	}

	public String getPolyString() {
		return polyString;
	}

	public void setPolyString(String polyString) {
		this.polyString = polyString;
	}

}
